package com.urdomain.ch02;

public class VariableScopeExample {
	public static void main(String[] args) {
		int v1 = 15;
		int v2 = 0;
		
		if(v1>10) {
			v2 = v1 - 10;
		}

		int v3 = v1 + v2 + 5;	//v2 변수를 사용할 수 없기 때문에 컴파일 에러가 생김
									//v2를 중괄호 밖에 만들어서 해결
		System.out.println("v3 = " + v3);
	}
}
